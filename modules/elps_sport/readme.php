本模組仍依彰化縣二林國小實際應用情況撰寫。但己盡力模擬各校的應用情形。
如有不適於貴校使用情況，可來信告知chi@mail.elps.chc.edu.tw

模組發展背景：
<FONT COLOR='#800000'>運動會時檢錄單的抄寫不勝其煩。
特別於決賽時，須及時的依預賽的成績再寫2份決賽檢錄單。
這對於大型學校須大量的人力處理，
另外製作秩序冊時，也須將各班的報名表重新輸入或繕寫。
設計這個模組並未將一切完全自動化，
但相較於傳統作業，它將是您可靠的左右手。</FONT>

模組應用方向：
1.製作秩序冊輔助。
2.檢錄單印製。

模組功能如下：
<FONT COLOR='#800000'>1.提供級任教師(導師)報名各個開辦項目，並輸入運動員編號。
2.提供大會人員報名各個開辦項目，並輸入運動員編號。
3.提供大會人員自動輸入全班或全年級運動員編號。
4.依設定時間控管可操作時間(各班、大會中心)。
5.提供製作秩序冊的相關人員名單，參賽項目選手名冊列表、各班選手名冊列表。
6.多樣性的檢錄方式(即安排跑道順序)。
7.方便的輸入方式，您在輸入成績或道次的欄位可以方便的使用上下方向鍵移動。</FONT>

使用流程提示：
<b style='COLOR:red'>※資料表格曾於93年4月18日異動，正式使用前請先將本模組全部移除後(含資料庫資料)，
再安裝一次即可，若是於此日期後安裝本模組則無必要。</b>
<FONT COLOR='#800000'>1.開辦主題設定操作時間(網管或授權人員)。--[比賽管理/比賽管理]
2.操作授權大會人員。--[比賽管理/人員授權]
3.大會人員開辦主題內的各項比賽組別。--[比賽管理/比賽項目管理]
4.各班導師報名或填寫編號(亦可全部由大會人員處理)。--[比賽報名(導師)或 報名管理(大會)/ 比賽管理]
5.編排選手跑道序(這時就導師無法報名了，但大會可以)。--[報名管理(大會)/初賽檢錄]
6.列印初賽檢錄單、運動員名冊、各項目參賽名冊--[報名管理(大會)/初賽檢錄、名冊輸出]
  (自行配合文書處理軟體整合出秩序冊)。
       <B>檢錄單列印時：</B>
       <FONT COLOR='blue'>Mozilla系列流覽器，頁面設定上下左右全設為5mm，標頭與註腳全設空白。
       IE系列流覽器，設定列印格式請將頁首頁尾全部清空。</FONT>
7.成績輸入。--[報名管理(大會)/成績輸入]
8.晉級檢錄、列印決賽檢錄單。--[報名管理(大會)/晉級檢錄]
